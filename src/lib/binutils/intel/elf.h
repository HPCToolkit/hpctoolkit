#ifndef PTI_SAMPLES_UTILS_ELF_H_
#define PTI_SAMPLES_UTILS_ELF_H_

#include <stdint.h>

#define ELF_MAGIC_NUMBER 0x7F
#define ELF_NIDENT       16

struct Elf64Header {
  unsigned char ident[ELF_NIDENT];
  uint16_t type;
  uint16_t machine;
  uint32_t version;
  uint64_t entry;
  uint64_t phoff;
  uint64_t shoff;
  uint32_t flags;
  uint16_t ehsize;
  uint16_t phentsize;
  uint16_t phnum;
  uint16_t shentsize;
  uint16_t shnum;
  uint16_t shstrndx;
};

struct Elf64SectionHeader {
  uint32_t name;
  uint32_t type;
  uint64_t flags;
  uint64_t addr;
  uint64_t offset;
  uint64_t size;
  uint32_t link;
  uint32_t info;
  uint64_t addralign;
  uint64_t entsize;
};

#endif // PTI_SAMPLES_UTILS_ELF_H_
